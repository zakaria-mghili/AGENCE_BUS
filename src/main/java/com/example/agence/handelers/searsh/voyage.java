package com.example.agence.handelers.searsh;

public class voyage {
    private String arrivalcityid;
    private String arrivaltimeid;
    private String departurecityid;
    private String departuretimeid;
    private String numberpassengerid;
    private String priceid;

    public String getArrivalcityid() {
        return arrivalcityid;
    }
    public void setArrivalcityid(String arrivalcityid) {
        this.arrivalcityid = arrivalcityid;
    }

    public String getArrivaltimeid() {
        return arrivaltimeid;
    }
    public void setArrivaltimeid(String arrivaltimeid) {
        this.arrivaltimeid = arrivaltimeid;
    }

    public String getDeparturecityid() {
        return departurecityid;
    }
    public void setDeparturecityid(String departurecityid) {
        this.departurecityid = departurecityid;
    }

    public String getDeparturetimeid() {
        return departuretimeid;
    }
    public void setDeparturetimeid(String departuretimeid) {
        this.departuretimeid = departuretimeid;
    }

    public String getNumberpassengerid() {
        return numberpassengerid;
    }
    public void setNumberpassengerid(String numberpassengerid) {
        this.numberpassengerid = numberpassengerid;
    }

    public String getPriceid() {
        return priceid;
    }
    public void setPriceid(String priceid) {
        this.priceid = priceid;
    }
}
